--Триггер: автоматическое обновление total_amount в orders
CREATE OR REPLACE TRIGGER trg_update_order_total
AFTER INSERT OR UPDATE OR DELETE ON order_items
FOR EACH ROW
DECLARE
    v_order_id orders.id%TYPE;
BEGIN
    -- Определяем ID заказа
    IF INSERTING THEN
        v_order_id := :NEW.order_id;
    ELSIF UPDATING THEN
        v_order_id := :NEW.order_id;
    ELSE
        v_order_id := :OLD.order_id;
    END IF;
    -- Обновляем общую сумму заказа
    UPDATE orders
    SET total_amount = (
        SELECT NVL(SUM(quantity * price_at_moment), 0)
        FROM order_items
        WHERE order_id = v_order_id
    )
    WHERE id = v_order_id;
END;


