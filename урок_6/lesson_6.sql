-- Триггер на изменение цены
CREATE OR REPLACE TRIGGER trg_log_price_changes
AFTER UPDATE OF price ON dishes
FOR EACH ROW
BEGIN
    IF :OLD.price != :NEW.price THEN
        INSERT INTO price_change_log (dish_id, old_price, new_price, changed_by)
        VALUES (:OLD.id, :OLD.price, :NEW.price, USER);
    END IF;
END;
